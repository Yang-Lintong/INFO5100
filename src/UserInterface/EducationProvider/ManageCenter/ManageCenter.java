    public void populateCourseTable(){
        DefaultTableModel model = (DefaultTableModel) CourseRequestTbl.getModel();
        
        model.setRowCount(0);
        
        for(CourseRequest cr:(ArrayList<CourseRequest>) manageCenterOrganization.getWorkQueue().getWorkRequestMap().get(WorkRequest.Type.CourseRequest)){
            Object[] row = new Object[6];
            row[0]=cr.getCourse().getID();
            row[1]=cr.getCourse().getWeekDay();
            row[2]=cr.getCourse().getDescription();
            row[3]=cr.getCourse().getStartDate();
            row[4]=cr.getCourse().getEndDate();
            row[5]=cr.getSender();
            row[6]=cr.getReciever();
            row[7]=cr.getStatus();
            row[8]=cr.getVolunteer().getPersonID();
            
        }
    }
