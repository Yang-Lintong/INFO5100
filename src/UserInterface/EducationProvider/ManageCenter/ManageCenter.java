    private void VolunteerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VolunteerBtnActionPerformed
        int selectedRow = CourseRequestTbl.getSelectedRow();
        if (selectedRow < 0){
            return;
        }
        CourseRequest cr = (CourseRequest)CourseRequestTbl.getValueAt(selectedRow, 0);  
        Course course = cr.getCourse();
        VolunteerRequestHistory volunteerRequestHistory = manageCenterOrganization.getVolunteerRequestHistory();
        VolunteerRequest vr = volunteerRequestHistory.addVolunteerRequest();
        vr.setCourse(course);
        vr.setSender(account);
        vr.setCourseRequest(cr);// TODO add your handling code here:
    }//GEN-LAST:event_VolunteerBtnActionPerformed

