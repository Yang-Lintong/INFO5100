    public ArrayList<Classroom> filterClassroom(){
        ArrayList<Classroom>  temlist= new ArrayList<Classroom>();
        for(Classroom classroom:sponsorAdvertisingOrganization.getClassroomList().getClassroomList()){
            if (classroom.getSponsorName().equals(enterprise.getName())){
                temlist.add(classroom);
            }
        }
        return temlist;
    }
    
    public void populateClassroomTable(){
    DefaultTableModel model = (DefaultTableModel) ClassroomTbl.getModel();
        
        model.setRowCount(0);
        ArrayList<Classroom> filterList=filterClassroom();
        for(Classroom classroom:filterList){
            Object[] row = new Object[6];
            row[0] = classroom.getID();
            row[1] = classroom;
            row[2] = classroom.getAccomodation();
            row[3] = classroom.getAvailWeekday();
            row[4] = classroom.getAvailStart();
            row[5] = classroom.getAvailEnd();
            
         
            model.addRow(row);
        }
    }
    
     public void populateClassTable(){
        
        DefaultTableModel model = (DefaultTableModel) classroomRequestTbl.getModel();
        
        model.setRowCount(0);
        for (ClassroomRequest request :(ArrayList<ClassroomRequest>) organization.getWorkQueue().getWorkRequestMap().get(WorkRequest.Type.ClassroomRequest)){
            Object[] row = new Object[6];
            row[0] = request.getCourse().getID();
            row[1] = request.getClassroom().getID();
            row[2] = request.getPublishDate();
            row[3] = request.getSender();
            row[4] = request.getReciever();
            row[5] = request.getClassroom().getAccomodation();
         
            model.addRow(row);
        }
        
  
    }
